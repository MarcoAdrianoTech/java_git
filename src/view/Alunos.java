    public void listar() {

        alunosDao dao = new alunosDao();
        List<AlunosModel> lista = dao.listarAlunos();
        DefaultTableModel dados = (DefaultTableModel) tabelaAluno.getModel();
        dados.setNumRows(0);
        for (AlunosModel c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf(),
                c.getPlano(),
                c.getMensalidade(),
                c.getIdade()});
        }
    }

