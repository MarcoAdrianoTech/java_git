    private void ButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarActionPerformed
        // botao salvar
        
        // 1 passo pega os dados que foram digitado tela view  e armazenar dentro do objeto  AlunosModel 
        // criar o bjeto 
        AlunosModel obj = new AlunosModel();
        
        //2  pega os dados guardar dentro do obj 

        obj.setNome(txtnome.getText());// pega da caixinha txtnome e armazenar dentro do atributo setnome dentro do objeto  
        obj.setRg(txtrg.getText());
        obj.setCpf(txtcpf.getText());
        obj.setEmail(txtemail.getText());
        obj.setTelefone(txttelefone.getText());
        obj.setCelular(txtcelular.getText());
        obj.setCep(txtcep.getText());
        obj.setEndereco(txtendereco.getText());
        obj.setNumero(Integer.parseInt(txtnumero.getText()));
        obj.setComplemento(txtcomplemento.getText());
        obj.setBairro(txtbairro.getText());
        obj.setCidade(txtcidade.getText());
        obj.setUf(cbuf.getSelectedItem().toString());
        obj.setPlano(cbplano.getSelectedItem().toString());
        obj.setMensalidade(Integer.parseInt(txtmensalidade.getText()));
        obj.setIdade(Integer.parseInt(txtidade.getText()));
        obj.setId(Integer.parseInt(txtcodigo.getText()));
        
       // 3 passo criar objeto camada dao para cadastrar esse obj model no banco de dados 
        alunosDao dao = new alunosDao();
        
        new Utilitarios().LimpaTela(PanelCadastroAluno);
        if (obj.getIdade() > 10) {

            dao.cadastrarAluno(obj);// metodo cadastrarAluno passando obj 
        } else {

            JOptionPane.showMessageDialog(null, "Aluno não pode ser cadastrado idade inferior 10 anos ");
        }


    }//GEN-LAST:event_ButtonSalvarActionPerformed

