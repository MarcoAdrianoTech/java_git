    private void ButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarActionPerformed
        // botao buscar por nome
        String nome = txtnome.getText();
        FuncionarioModel obj = new FuncionarioModel();
        FuncionarioDao dao = new FuncionarioDao();

        obj = dao.consultarPorNome(nome);

        if (obj.getNome() != null) {

            txtcodigo.setText(String.valueOf(obj.getId()));
            txtnome.setText(obj.getNome());
            txtrg.setText(obj.getRg());
            txtcpf.setText(obj.getCpf());
            txtemail.setText(obj.getEmail());
            txtSenha.setText(obj.getSenha());
            txtcargo.setText(obj.getSenha());
            cbNivel_Acesso.setSelectedItem(obj.getNivel_Acesso());
            txttelefone.setText(obj.getTelefone());
            txtcelular.setText(obj.getCelular());
            txtcepfuncionario.setText(obj.getCep());
            txtendereco.setText(obj.getEndereco());
            txtnumero.setText(String.valueOf(obj.getNumero()));
            txtsalario.setText(obj.getComplemento());
            txtcargo.setText(obj.getComplemento());
            txtbairro.setText(obj.getBairro());
            txtcidade.setText(obj.getCidade());
            cbuf.setSelectedItem(obj.getUf());
           txtsalario.setText(String.format("%.2f",obj.getSalario()));

        } else {
            JOptionPane.showMessageDialog(null, "Aluno não encontrado");
        }

    }//GEN-LAST:event_ButtonBuscarActionPerformed

