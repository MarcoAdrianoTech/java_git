    private void ButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarActionPerformed
        // botao salvar

        FuncionarioModel obj = new FuncionarioModel();

        obj.setNome(txtnome.getText());
        obj.setRg(txtrg.getText());
        obj.setCpf(txtcpf.getText());
        obj.setEmail(txtemail.getText());
        obj.setSenha(txtSenha.getText());
        obj.setCargo(txtcargo.getText());
        obj.setNivel_Acesso(cbNivel_Acesso.getSelectedItem().toString());
        obj.setTelefone(txttelefone.getText());
        obj.setCelular(txtcelular.getText());
        obj.setCep(txtcepfuncionario.getText());
        obj.setEndereco(txtendereco.getText());
        obj.setNumero(Integer.parseInt(txtnumero.getText()));
        obj.setComplemento(txtcomplementofun1.getText());
        obj.setBairro(txtbairro.getText());
        obj.setCidade(txtcidade.getText());
        obj.setUf(cbuf.getSelectedItem().toString());
        obj.setSalario(Double.parseDouble(txtsalario.getText()));

        obj.setId(Integer.parseInt(txtcodigo.getText()));
        
        FuncionarioDao dao = new FuncionarioDao();
   
        new Utilitarios().LimpaTela(PanelCadastroFuncionarios);
        
        if(obj.getSalario()>0){
        
             dao.cadastrarFuncionario(obj);
        
        }else{
        
             JOptionPane.showMessageDialog(null, "não aceita numeros negativo");
        
        }
      

    }//GEN-LAST:event_ButtonSalvarActionPerformed

