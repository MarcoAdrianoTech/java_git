    public void listar() {

        FuncionarioDao dao = new FuncionarioDao();
        List<FuncionarioModel> lista = dao.listarFuncionario();
        // criando os objecto dados para armazernar na tabelafuncionari
        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionario.getModel(); 
        
        dados.setNumRows(0);// limpa os dados 
        
        // para cada item desta lista ele vai criar um objecto chamaodo c e esse c adiciono na lista 
        for (FuncionarioModel c : lista) {
            dados.addRow(new Object[]{// pra cada linha eu vo adiconar um novo vetor de objeto 
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getSenha(),
                c.getCargo(),
                c.getNivel_Acesso(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf(),
                c.getSalario(),});
        }
        
    }
