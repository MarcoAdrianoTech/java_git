    private void ButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarActionPerformed
        // botao buscar por nome
        String nome = txtnomeprof.getText();
        ProfessoresModel obj = new ProfessoresModel();
        ProfessorDao dao = new ProfessorDao();

        obj = dao.consultarProfessorPorNome(nome);

        if (obj.getNome() != null) {

            txtnomeprof.setText(obj.getNome());
            txtcpf.setText(obj.getCpf());
            txttelefoneprof.setText(obj.getTelefone());
            txtcepprof.setText(obj.getCep());
            txtenderecoprof.setText(obj.getEndereco());
            txtnumeroprof.setText(String.valueOf(obj.getNumero()));
            txthoras.setText(String.valueOf(obj.getHoras()));
            txtbairroprof.setText(String.valueOf(obj.getBairro()));
            txtvalor.setText(String.valueOf(obj.getValor()));
            txtcodigo.setText(String.valueOf(obj.getId()));
            // obj.setId(Integer.parseInt(txtcodigo.getText()));

        } else {
            JOptionPane.showMessageDialog(null, "Professor n√£o encontrado");
        }

    }//GEN-LAST:event_ButtonBuscarActionPerformed

