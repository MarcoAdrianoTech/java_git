    private void ButtonPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonPesquisaActionPerformed
        // pesquisa por nome
        String nome = "%" + txtpesquisa.getText() + "%";//sintaxe do sql % caracter qualquer coisa antes e depois
        ProfessorDao dao = new ProfessorDao();
        List<ProfessoresModel> lista = dao.buscaProfessorPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaProfessor.getModel();
        dados.setNumRows(0);
        for (ProfessoresModel c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCpf(),
                c.getTelefone(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getBairro(),
                c.getHoras(),
                c.getValor()});
        }


    }//GEN-LAST:event_ButtonPesquisaActionPerformed

