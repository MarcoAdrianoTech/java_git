    private void BotaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoSalvarActionPerformed
        // botao salvar

        ProfessoresModel obj = new ProfessoresModel();

        obj.setNome(txtnomeprof.getText());
        obj.setCpf(txtcpf.getText());
        obj.setEndereco(txtenderecoprof.getText());
        obj.setNumero(Integer.parseInt(txtnumeroprof.getText()));
        obj.setBairro(txtbairroprof.getText());
        obj.setCep(txtcepprof.getText());
        obj.setTelefone(txttelefoneprof.getText());
        obj.setHoras(Integer.parseInt(txthoras.getText()));
        obj.setValor(Integer.parseInt(txtvalor.getText()));

        ProfessorDao dao = new ProfessorDao();
        dao.cadastrar(obj);
        new Utilitarios().LimpaTela(PanelCadastroProfessor);


    }//GEN-LAST:event_BotaoSalvarActionPerformed

